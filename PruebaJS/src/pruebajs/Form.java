    private void btnCargarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCargarMouseClicked
        dtm.addRow(new Object[]{txtMarca.getText(), txtModelo.getText(), txtAnio.getText(),txtPrecio.getText()});
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        int anio = Integer.parseInt(txtAnio.getText());
        int precio = Integer.parseInt(txtPrecio.getText());
        Auto1 auto1 = new Auto1(marca, modelo, anio, precio);
        Autos.add(auto1);
        System.out.println(Autos);

    }//GEN-LAST:event_btnCargarMouseClicked

    private void btnModifMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModifMouseClicked
        int fila = tblAutos.getSelectedRow();
        for (int i = 0; i < Autos.size(); i++) {
            dtm.getValueAt(fila, 1).equals(Autos.get(i).getModelo());
            Autos.get(i).setMarca(txtMarca.getText());
            Autos.get(i).setModelo(txtModelo.getText());
            Autos.get(i).setAnio(Integer.parseInt(txtAnio.getText()));
            Autos.get(i).setPrecio(Integer.parseInt(txtPrecio.getText()));
            System.out.println(Autos);
        }
        dtm.setValueAt(txtMarca.getText(), fila, 0);
        dtm.setValueAt(txtModelo.getText(), fila, 1);
        dtm.setValueAt(txtAnio.getText(), fila, 2);
        dtm.setValueAt(txtPrecio.getText(), fila, 3);
        
    }//GEN-LAST:event_btnModifMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        int fila = tblAutos.getSelectedRow();
        for (int i = 0; i < Autos.size(); i++) {
            dtm.getValueAt(fila, 1).equals(Autos.get(i).getModelo());
            Autos.remove((Autos.get(i)));
            System.out.println(Autos);
        }
        dtm.removeRow(fila);
    }//GEN-LAST:event_btnEliminarMouseClicked

