    private void btnCambiar_DatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCambiar_DatosMouseClicked
       
       int fila = tblDatos.getSelectedRow();
       int precio= Integer.parseInt(txtprecio.getText());
       int cantidad=Integer.parseInt(txtcantidad.getText());
       float precio_total= precio*cantidad;
        dtm.setValueAt(txtNombre.getText(), fila, 0);
       dtm.setValueAt(txtcantidad.getText(), fila, 1);
       dtm.setValueAt(txtprecio.getText() , fila , 2);
       dtm.setValueAt(precio_total , fila , 3);

        Productos.get(fila).setNombre(txtNombre.getText());
       Productos.get(fila).setCantidad(Integer.parseInt(txtcantidad.getText()));
       Productos.get(fila).setPrecio(Integer.parseInt(txtprecio.getText()));
       Productos.get(fila).setPrecioTotal(precio_total);
      
       
     
    }//GEN-LAST:event_btnCambiar_DatosMouseClicked

