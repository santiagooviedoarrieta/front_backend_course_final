    private void btnCargarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCargarMouseClicked
      String Nombre= txtNombre.getText();
      int Cantidad = Integer.parseInt(txtcantidad.getText());
      int Precio= Integer.parseInt(txtprecio.getText());
      float precio_total= Precio*Cantidad;
      Producto Producto = new Producto(Nombre , Cantidad , Precio , precio_total);
      dtm.addRow(new Object[]{txtNombre.getText(), txtcantidad.getText(), txtprecio.getText() , precio_total });
      Productos.add(new Producto(Producto.getNombre(),Producto.getCantidad() , Producto.getPrecio() , Producto.getPrecioTotal()));
      System.out.println(Productos);
    }//GEN-LAST:event_btnCargarMouseClicked

